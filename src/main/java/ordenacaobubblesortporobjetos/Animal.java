public class Animal {
    private String especie;
    private String nome;
    public Animal(String especie, String nome) {
    this.especie = especie;
    this.nome = nome;
    }
 
    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}; // Final classe
    

